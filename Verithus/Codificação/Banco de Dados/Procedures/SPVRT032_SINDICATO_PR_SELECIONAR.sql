CREATE PROCEDURE [dbo].[SPVRT032_SINDICATO_PR_SELECIONAR]
(
	@CODIGO_USUARIO_CADASTRO	INT = NULL,
	@CODIGO_SINDICATO		 INT = NULL
)
AS
BEGIN
	SELECT * FROM TBVRT029_SINDICATO WHERE 
		(CODIGO_SINDICATO = @CODIGO_SINDICATO OR @CODIGO_SINDICATO IS NULL) 
	IF(@@ERROR <>0)
		BEGIN
			SELECT  @@ERROR as Erro, 'ERRO NO SELECT DA TABELA TBVRT029_SINDICATO.' as Mensagem 

