-- TBVRT021_TIPO_SECAO
CREATE PROCEDURE [dbo].[SPVRT082_TIPO_SECAO_PR_INCLUIR]
(
	@DESCRICAO					nvarchar(80)	=   null,
	@CODIGO_USUARIO_CADASTRO	int				=   null,
	@DATA_CADASTRO				datetime		=	null,
	@CODIGO_USUARIO_ALTERACAO int				=   null,
	@DATA_ALTERACAO			datetime		    =   null,
	@CODIGO_STATUS				int             =	null
)
AS
BEGIN

	INSERT INTO TBVRT021_TIPO_SECAO(
		DESCRICAO,
		CODIGO_USUARIO_CADASTRO,
		DATA_CADASTRO,
		CODIGO_USUARIO_ALTERACAO,
		DATA_ALTERACAO,
		CODIGO_STATUS	
	)
	VALUES(
		@DESCRICAO,
		@CODIGO_USUARIO_CADASTRO,
		@DATA_CADASTRO,
		@CODIGO_USUARIO_ALTERACAO,
		@DATA_ALTERACAO,
		@CODIGO_STATUS
	)


		DECLARE @DESC VARCHAR(120)

	IF(@@ERROR <>0)
		BEGIN

			SET @DESC = 'ERRO AO INCLUIR UM TIPO DE SECAO - CODIGO DO ERRO' + CAST(@@ERROR AS VARCHAR)

			EXEC dbo.SPVRT168_LOG_PR_INCLUIR 'E', @DESC , @CODIGO_USUARIO_CADASTRO, 'INCLUSÃO DE TIPO DE SECAO';

			SELECT @@ERROR as Erro, @DESC as Mensagem
			RETURN
		END
	ELSE
		BEGIN
			
			SET @DESC = 'SUCESSO AO INCLUIR UM TIPO DE SECAO - CODIGO DO TIPO DE SECAO' + CAST(@@IDENTITY AS VARCHAR)

			EXEC dbo.SPVRT168_LOG_PR_INCLUIR 'A', '' , @CODIGO_USUARIO_CADASTRO, 'INCLUSÃO DE TIPO DE SECAO';

			SELECT * FROM  TBVRT021_TIPO_SECAO
			WHERE CODIGO_TIPO_SECAO = @@IDENTITY
			RETURN
		END

END